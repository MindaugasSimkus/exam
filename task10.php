<?php 

class numberList {
	public $numbers = [];

	public function add($number) {
		array_push($this->numbers, $number);
	}
	public function average() {
		$average = array_sum($this->numbers)/count($this->numbers);
		return $average;
	}
}

$list = new numberList();

print_r($list);

$list->add(12);

print_r($list);

$list->add(28);

print_r($list);

echo $list->average();

